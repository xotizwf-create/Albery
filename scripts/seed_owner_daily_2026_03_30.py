def main() -> int:
    backend.ensure_owner_reports_schema()
    target = TARGET_DATE
    input_payload = backend._build_owner_daily_input(target)
    analysis = dict(ANALYSIS)
    analysis["report_text"] = backend._build_owner_daily_report_text(analysis, target)
    raw_payload = {
        "model": "manual-curated",
        "input": input_payload,
        "analysis": analysis,
        "source": "manually crafted (Google API rate limited 04.05.2026)",
    }
    with backend.pg_connect() as conn:
        with conn.transaction():
            with conn.cursor() as cur:
                cur.execute(
                    "UPDATE owner_daily_reports SET is_current = FALSE WHERE report_date = %s AND is_current = TRUE",
                    (target,),
                )
                cur.execute(
                    "SELECT COALESCE(MAX(version), 0) + 1 AS version FROM owner_daily_reports WHERE report_date = %s",
                    (target,),
                )
                version = int(cur.fetchone()["version"])
                cur.execute(
                    """
                    INSERT INTO owner_daily_reports (
                        report_date, version, is_current, ai_request_id, prompt_id, generated_at,
                        summary, dynamics_summary, risks_summary, recommendations, report_text, raw_json
                    ) VALUES (%s, %s, TRUE, NULL, NULL, now(), %s, %s, %s, %s, %s, %s)
                    RETURNING id, version, generated_at
                    """,
                    (
                        target,
                        version,
                        analysis["summary"],
                        analysis["dynamics_summary"],
                        analysis["risks_summary"],
                        "\n".join(f"- {item}" for item in analysis["recommendations"]),
                        analysis["report_text"],
                        backend.pg_json(raw_payload),
                    ),
                )
                row = cur.fetchone()
    print("Saved owner_daily_report id:", row["id"])
    print("version:", row["version"])
    print("generated_at:", row["generated_at"])
    print()
    print("=== report_text ===")
    print(analysis["report_text"])
    return 0


